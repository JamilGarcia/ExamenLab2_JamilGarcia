    public void log(int id, String accion) throws IOException {
        Date fecha = new Date();
        File f = null;
        FileWriter fw = null;
        BufferedWriter bw = null;
        try {
            f = new File("./log.txt");
            fw = new FileWriter(f, true); //OJO 
            bw = new BufferedWriter(fw);
            bw.write("El ID: " + id + " " + accion + " justo a las: " + fecha  + "   ;   ");
            bw.flush();

        } catch (Exception e) {
            e.printStackTrace();
        }
        bw.close();
        fw.close();
    }

