    private void b_Mantenimiento_IngresarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_b_Mantenimiento_IngresarMouseClicked
        // TODO add your handling code here:
        boolean t = true;
        admn.cargarArchivo();

        for (int i = 0; i < admn.getListaMantenimiento().get(Num_Usuario).getAtm().size(); i++) {
            if (ID_ATM != admn.getListaMantenimiento().get(Num_Usuario).getAtm().get(i).getID_ATM()) {
                t = false;
            }
        }

        if (t) {
            String pass = JOptionPane.showInputDialog(this, "Ingrese la contraseña para cerrar");
            if (pass.equals(admn.getListaMantenimiento().get(Num_Usuario).getContra())) {
                int dinero1 = sp_mantenimiento_100.getComponentCount() * 100;
                int dinero2 = sp_mantenimiento_500.getComponentCount() * 500;
                adat.cargarArchivo();
                int temp = cb_ATMs.getSelectedIndex();
                int dineroATM = adat.getListaAtm().get(temp).getDinero();
                dineroATM += dinero1 + dinero2;
                adat.getListaAtm().get(temp).setDinero(dineroATM);
                try {
                    log(ID_ACTIVO, "Ingresó dinero al ATM");
                } catch (IOException ex) {
                    Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        } else {
            JOptionPane.showMessageDialog(this, "Error!!! NO TIENES PERMISO PARA USAR ESTE ATM");
            try {
                log(ID_ACTIVO, "Intentó ingresar a un ATM incorrecto");
            } catch (IOException ex) {
                Logger.getLogger(Main.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_b_Mantenimiento_IngresarMouseClicked

